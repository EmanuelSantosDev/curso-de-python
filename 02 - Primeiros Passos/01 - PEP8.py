""" ============= PEP 8 =============

O que são PEPs?

  - Python Enhancement Proposals (PEPs)
  - são arquivos que apresentam propostas de melhoria para a Linguagem Python
  - https://peps.python.org/
  - não necessariamente são os criadores do Python que criam as PEPs
  - elas passam por votação e nem sempre são aprovadas para aparecerem no site
  - PEP 8 -> Style Guide for Python Code

Regras mais utilizadas:

  1) O PyCharm vem com padrão de reconhecimento de palavras em inglês. Para resolver:

     - File -> settings -> lupa -> Inspections -> Proofreading -> Desmarque Typo

  2) Identação

     - Utilize 4 espaços para cada código dentro de blocos como funções, condicionais,
       classes, entro outros...

  3) Linha em Branco

    - Sempre deixar UMA LINHA EM BRANCO ao final do programa
    - Separe funções e classes com DUAS LINHAS EM BRANCO

  4) Importação

    - Se você deseja fazer importação de duas bibliotecas, importe uma por vez e logo no
      inicio do programa (após os comentarios iniciais e antes da declaração da primeira
      variável/função)

  5) Uso de Espaços

    - Nunca utilize espaços antes ou depois de chaves, colchetes ou parênteses
    - Use um espaço antes e depois quando declarar uma variável ou usar um operador
      do tipo <, >, ==, entre outros...

  6) Nomenclatura de Variáveis

    - utilizar letras minusculas separadas por underline
    - utilizar letras maiusculas separadas por underline
    - utilizar palavras começando com letras maiusculas (camelCase)

"""
